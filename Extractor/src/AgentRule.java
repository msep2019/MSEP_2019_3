class AgentRule {
	private String agent ;
	private String behaviour ;
	public AgentRule() {
		super() ;
	}
	
	//set behaviour
	public String getAgent() {
		return agent ;
	}
	
	public void setAgent(String agent) {
		this.agent = agent ;
	}
	
	//set vulnerability
	public String getBehaviour() {
		return behaviour ;
	}
	
	public void setBehaviour(String behaviour) {
		this.behaviour = behaviour ;
	}
}